package container

import (
	"strings"
	"testing"
)

func TestRenderEnvFile(t *testing.T) {
	header := "# generated, do not edit\n"
	env := map[string]string{
		"ZED":         "last",
		"ALPHA":       "first",
		"WITH_EQUALS": "Authorization=Bearer abc123", // value keeps its '='
		"URL":         "http://10.0.0.1:4318",
	}
	got := string(RenderEnvFile(header, env))

	// Header is written verbatim at the very top.
	if !strings.HasPrefix(got, header) {
		t.Errorf("header not at top:\n%s", got)
	}
	// Each entry is one KEY=value line, value verbatim (incl. embedded '=').
	for _, want := range []string{
		"\nALPHA=first\n",
		"\nURL=http://10.0.0.1:4318\n",
		"\nWITH_EQUALS=Authorization=Bearer abc123\n",
		"\nZED=last\n",
	} {
		if !strings.Contains(got, want) {
			t.Errorf("missing %q in:\n%s", want, got)
		}
	}
	// Keys are sorted deterministically.
	ai := strings.Index(got, "\nALPHA=")
	ui := strings.Index(got, "\nURL=")
	wi := strings.Index(got, "\nWITH_EQUALS=")
	zi := strings.Index(got, "\nZED=")
	if !(ai < ui && ui < wi && wi < zi) {
		t.Errorf("keys not sorted: a=%d u=%d w=%d z=%d", ai, ui, wi, zi)
	}
}

func TestRenderEnvFile_EmptyIsHeaderOnly(t *testing.T) {
	header := "# header\n"
	got := string(RenderEnvFile(header, map[string]string{}))
	if got != header {
		t.Errorf("empty map should render header only, got:\n%q", got)
	}
}

// TestRenderOTelEnvFile_DelegationParity guards that the OTel wrapper still
// produces the byte-for-byte output it did before the #492 extraction —
// header banner + sorted literal lines.
func TestRenderOTelEnvFile_DelegationParity(t *testing.T) {
	env := map[string]string{
		"OTEL_EXPORTER_OTLP_ENDPOINT": "http://10.0.3.5:4318",
		"OTEL_EXPORTER_OTLP_HEADERS":  "Authorization=Bearer abc123",
	}
	got := string(RenderOTelEnvFile(env))
	want := string(RenderEnvFile(otelEnvFileHeader, env))
	if got != want {
		t.Errorf("RenderOTelEnvFile diverged from RenderEnvFile:\n got=%q\nwant=%q", got, want)
	}
	if !strings.HasPrefix(got, "# Generated by Containarium") {
		t.Errorf("OTel header banner lost:\n%s", got)
	}
}
